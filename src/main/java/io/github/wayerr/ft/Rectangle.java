package io.github.wayerr.ft;

/**
 * rectangle representation <p/>
 *
 * Created by wayerr on 10.02.15.
 */
public class Rectangle {
    public int x;
    public int y;
    public int w;
    public int h;

    @Override
    public String toString() {
        return "Rectangle{" +
          "x=" + x +
          ", y=" + y +
          ", w=" + w +
          ", h=" + h +
          '}';
    }
}
